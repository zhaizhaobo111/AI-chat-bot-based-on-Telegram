DEFAULT_PRESET = "tsundere"


def get_preset(name: str) -> dict:
    return PRESETS.get(name, PRESETS[DEFAULT_PRESET])


def list_presets() -> str:
    lines = ["可选人设：\n"]
    for key, preset in PRESETS.items():
        lines.append(f"  /setmode {key} - {preset['name']}")
    return "\n".join(lines)
